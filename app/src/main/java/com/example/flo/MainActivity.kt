        songs[nowPos] = if (songId == 0){
            songDB.songDao().getSongs(1) //id가 0이면 저장된 song 데이터가 없었던 것이므로 첫번째 데이터 실행
        }else{
            songDB.songDao().getSongs(songId)
        }

        Log.d("song ID", songs[nowPos].id.toString())
        setMiniPlayer(songs[nowPos])
    }

